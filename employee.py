class Employee:
    def __init__(
        self,
        registration_number,
        age,
        gender,
        education_level,
        job_title,
        years_of_experience,
        salary,
    ):
        self.registration_number = registration_number
        self.age = age
        self.gender = gender
        self.education_level = education_level
        self.job_title = job_title
        self.years_of_experience = years_of_experience
        self.salary = salary